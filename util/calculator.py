class Calculator():

    def addition(self, a, b):
        return a + b

    def subtraction(self, a, b):
        return a - b

    def multiplication(self, a, b):
        return a * b

    def division(self, a, b):
        return a / b

    def simple_interest(self, principal, numOfYears, rate):
        return (principal * numOfYears * rate) / 100